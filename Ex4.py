import random
goal = random.randint(0, 1000)
count = 1
while (True):
    if count > 10:
        print("Failed! Goal number is", goal)
        break
    print("Try #", count, "!", sep="")
    res = input("Enter your number(0 - 1000): ")
    try:
        num = int(res)
        if (num >= 0 and num <= 1000):
            if num == goal:
                print("Congratulations! You guessed the number")
                break
            elif num > goal:
                print("Try less")
            else:
                print("Try greater")
        else:
            print("Number must be positive and less than 1001")
    except ValueError:
        print("That's not a number!")
    count += 1
